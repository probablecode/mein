#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nbr[11];
	int		sign;
	int		i;

	sign = (0 < n) - (n < 0);
	i = 10;
	while (n != 0)
	{
		nbr[i--] = (n % 10) * sign + '0';
		n /= 10;
	}
	if (sign <= 0)
		nbr[i--] = (sign < 0) * '-' + (sign == 0) * '0';
	i++;
	write (fd, nbr + i, 11 - i);
}
